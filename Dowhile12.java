// WAP to print 100 to 1 no.

package loop;

public class Dowhile12
{
    
     public static void main(String[] args) 
     {
       
        System.out.print("The numbers from 100 to 1 no:");

        int iNo = 100;

         
          do
          {
            {
              System.out.print(iNo+" ");
            }

            iNo--;
          } 
          while( iNo >= 1);   

    }
    
}
