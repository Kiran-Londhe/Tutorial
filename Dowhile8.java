// WAP to print 50 to 100 no

package loop;

public class Dowhile8 
{
  
     public static void main(String[] args) 
     {

      System.out.print("50 to 100 no are: ");

        int iNo = 50;
         
        do
          {
            System.out.print(iNo +" ");
            iNo++;
          }
          while( iNo <= 100);
         
          
    }
    
}