// WAP to print 1 to 100 no.

package loop;

public class Dowhile11
{
    
     public static void main(String[] args) 
     {
       
        System.out.print("The numbers from 1 to 100:");

        int iNo = 1;

          
          do
          {
           {
            System.out.print(iNo+" ");
           }
           
            iNo++;
          }  
          while( iNo <= 100);  

    }
    
}
